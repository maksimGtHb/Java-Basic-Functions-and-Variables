public class If_statement {
        public static void main (String[] args){
            int myInt = 6;
            if (myInt == 4) {
                System.out.println("myInt equals 4");
            }
            else if (myInt == 6) {
                System.out.println("myInt equals 6");
            }
            else {
                System.out.println("myInt not equals 4 or 6");
            }
        }
    }

